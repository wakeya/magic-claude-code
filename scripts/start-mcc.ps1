param(
    [string]$DataDir = ".\data",
    [string]$Password = ""
)

$ErrorActionPreference = "Stop"
$Root = Split-Path -Parent $MyInvocation.MyCommand.Path
Set-Location $Root

if (-not [string]::IsNullOrWhiteSpace($Password)) {
    $env:ADMIN_PASSWORD = $Password
}

$Exe = Join-Path $Root "mcc.exe"
if (-not (Test-Path $Exe)) {
    throw "mcc.exe not found: $Exe"
}

$DataPath = $DataDir
if (-not [System.IO.Path]::IsPathRooted($DataPath)) {
    $DataPath = Join-Path $Root $DataPath
}

$LogDir = Join-Path $Root "logs"
New-Item -ItemType Directory -Force -Path $LogDir | Out-Null

$Stamp = Get-Date -Format "yyyyMMdd-HHmmss"
$OutLog = Join-Path $LogDir "mcc-$Stamp.out.log"
$ErrLog = Join-Path $LogDir "mcc-$Stamp.err.log"

$Process = Start-Process `
    -FilePath $Exe `
    -ArgumentList @("-data", "`"$DataPath`"") `
    -WorkingDirectory $Root `
    -RedirectStandardOutput $OutLog `
    -RedirectStandardError $ErrLog `
    -WindowStyle Hidden `
    -PassThru

$Process.Id | Set-Content -Path (Join-Path $Root "mcc.pid")

Write-Host "mcc started. PID: $($Process.Id)"
Write-Host "Admin URL: https://localhost:8442"
if ([string]::IsNullOrWhiteSpace($env:ADMIN_PASSWORD)) {
    Write-Host "Password: randomly generated by mcc.exe. Check stdout log for the generated admin password."
}
Write-Host "stdout log: $OutLog"
Write-Host "stderr log: $ErrLog"
